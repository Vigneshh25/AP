package set6;

public class MagicNumber {
   public static void main(String[] args) 
{
      
    // Accepting sample input
    int x = 1234;
  
    // Condition to check Magic number
    if (x % 9 == 1)
        System.out.printf("Magic Number");
    else
        System.out.printf("Not a Magic Number");
}
}
